extension UIColor {
    static var mainWhiteColor: UIColor {
        return UIColor(redInt: 255, greenInt: 255, blueInt: 248, alpha: 1.0)
    }

    static var mainBlackColor: UIColor {
        return UIColor(redInt: 47, greenInt: 49, blueInt: 51, alpha: 1.0)
    }

    static var mainOrangeColor: UIColor {
        return UIColor(redInt: 255, greenInt: 109, blueInt: 29, alpha: 1.0)
    }

    static var additionalGoldColor: UIColor {
        return UIColor(redInt: 250, greenInt: 171, blueInt: 22, alpha: 1.0)
    }
}
